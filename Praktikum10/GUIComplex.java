    private void jButtonInsertDataListActionPerformed(java.awt.event.ActionEvent evt) {                                                      
        // TODO add your handling code here:
        dlm.clear();
        for (Map.Entry<Integer, String> entry : itemsMap.entrySet()) {
            dlm.addElement(entry.getValue());
        }
    }                                                     

    private void jButtonSaveDataListActionPerformed(java.awt.event.ActionEvent evt) {                                                    
        // TODO add your handling code here:
        itemsMap.clear();
        for (int i = 0; i < dlm.getSize(); i++) {
            String value = dlm.getElementAt(i);
            int key = i+1;
            itemsMap.put( key,  value);
        }
        updateJumDataMapTersimpan();
    }                                                   

