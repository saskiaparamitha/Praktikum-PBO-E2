    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {                                              
        // TODO add your handling code here:
        int index =jListItem.getSelectedIndex();
        String selected = jTextFieldNamaItem.getText();
        dlm.setElementAt(selected, index);
        jTextFieldNamaItem.setText("");
    }                                             

    private void jButtonInsertDataMapActionPerformed(java.awt.event.ActionEvent evt) {                                                     
        // TODO add your handling code here:
        dlm.clear();
        for (Map.Entry<Integer, String> entry : itemsMap.entrySet()) {
            dlm.addElement(entry.getValue());
        }
    }                                                    

    private void jButtonSaveDataMapActionPerformed(java.awt.event.ActionEvent evt) {                                                   
        // TODO add your handling code here:
        itemsMap.clear();
        for (int i = 0; i < dlm.getSize(); i++) {
            String value = dlm.getElementAt(i);
            int key = i+1;
            itemsMap.put( key,  value);
        }
        updateJumDataMapTersimpan();
    }                                                  

    private void jButtonInsertDataSetActionPerformed(java.awt.event.ActionEvent evt) {                                                     
        // TODO add your handling code here:
        for (String item : itemsSet) {
            dlm.addElement(item);
        }
    }                                                    

    private void jButtonSaveDataSetActionPerformed(java.awt.event.ActionEvent evt) {                                                   
        // TODO add your handling code here:
        if (!itemsSet.isEmpty()){
            itemsSet.clear();
        }
        for (int i = 0; i < dlm.getSize(); i++) {
            itemsSet.add(dlm.getElementAt(i));
        }
        updateJumDataSetTersimpan();
    }                                                  

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {                                              
        // TODO add your handling code here:
        int index= jListItem.getSelectedIndex();
                
        dlm.removeElementAt(index);
                
        jTextFieldNamaItem.setText("");
    }                                             
    
